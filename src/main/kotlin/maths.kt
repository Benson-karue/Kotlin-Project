fun main() {
    add(23,34)
    sub(23,34)
    multiply(23,34)
    division(23,34)
    modulus(23,34)

}

fun add(num1:Int,num2:Int){
    println(num1 + num2)
}

fun sub(num1:Int,num2:Int){
    println(num1 - num2)
}

fun multiply(num1:Int,num2:Int){
    println(num1 * num2)
}

fun division(num1:Int,num2:Int){
    println(num1 / num2)
}

fun modulus(num1:Int,num2:Int){
    println(num1 % num2)
}