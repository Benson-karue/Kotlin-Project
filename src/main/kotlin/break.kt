fun main(){

    // break in while loops increasing numbers
    var number = 20

    while(number<=30){
        println(number)
        number ++

        if (number ==27){
            break
        }
    }
    // break in loops decreasing numbers
    var num =50

    while(num >=40) {
        println(num)
        num --

        if (num == 45) {
             break
        }
    }
}